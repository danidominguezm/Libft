#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char			*s_tmp;
	size_t					i;

	i = -1;
	s_tmp = (unsigned char *)s;
	while (++i < n)
		*(s_tmp + i) = 0;
}
