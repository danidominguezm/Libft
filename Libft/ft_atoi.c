#include "libft.h"

int	ft_atoi(const char *str)
{
	 int i;
	 int j;
	 int res;

	res = 0;
	i = 0;
	
	while (!(*(str + i) >= '0' && *(str + i) <= '9') && *(str + i) != '-')
		i++;

	j = (*(str + i) == '-' ? -1 : 0);

	while (!(*(str + i) >= '0' && *(str + i) <= '9'))
		i++;

	while (*(str + i) >= '0' && *(str + i) <= '9')
	{
		res *= 10;
		res += *(str + i) - '0';
		i++;
	}

	return (res * j);
}
